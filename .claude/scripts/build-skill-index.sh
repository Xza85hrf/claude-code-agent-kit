#!/usr/bin/env bash
# build-skill-index.sh — Generate skill indexes from skills.yml
# Usage: bash .claude/scripts/build-skill-index.sh
#
# Generates:
#   .claude/skills/SKILL-INDEX.md    — Human-readable table
#   .claude/skills/skill-routes.json — Machine-readable routing data

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
SKILLS_DIR="$PROJECT_ROOT/.claude/skills"
REGISTRY="$SKILLS_DIR/skills.yml"
INDEX_MD="$SKILLS_DIR/SKILL-INDEX.md"
ROUTES_JSON="$SKILLS_DIR/skill-routes.json"

# ── Check prerequisites ──
if [[ ! -f "$REGISTRY" ]]; then
  echo "ERROR: $REGISTRY not found" >&2
  exit 1
fi

if ! python3 -c "import yaml" 2>/dev/null; then
  echo "PyYAML not found, installing..." >&2
  pip install --quiet pyyaml 2>/dev/null || pip3 install --quiet pyyaml 2>/dev/null || {
    echo "ERROR: Failed to install PyYAML. Install manually: pip install pyyaml" >&2
    exit 1
  }
fi

echo "Building skill indexes from $REGISTRY ..."

# ── Generate both outputs via a single Python invocation ──
SKILLS_DIR="$SKILLS_DIR" INDEX_MD="$INDEX_MD" ROUTES_JSON="$ROUTES_JSON" \
python3 << 'PYEOF'
import yaml
import json
import os
import sys

skills_dir = os.environ["SKILLS_DIR"]
index_md = os.environ["INDEX_MD"]
routes_json = os.environ["ROUTES_JSON"]
registry = os.path.join(skills_dir, "skills.yml")

with open(registry, "r") as f:
    data = yaml.safe_load(f)

skills = data.get("skills", [])
if not skills:
    print("ERROR: No skills found in registry", file=sys.stderr)
    sys.exit(1)

# ── Validate skill directories exist ──
warnings = []
for s in skills:
    sid = s["name"]
    dept = s.get("department", "")
    dept_path = os.path.join(skills_dir, dept, sid)
    symlink_path = os.path.join(skills_dir, sid)
    if not os.path.isdir(dept_path) and not os.path.isdir(symlink_path):
        warnings.append(f"  WARNING: No directory found for skill '{sid}' (checked {dept}/{sid} and {sid})")

if warnings:
    print("Directory validation warnings:")
    for w in warnings:
        print(w)
    print()

# ── Group by department ──
by_dept = {}
for s in skills:
    dept = s.get("department", "uncategorized")
    by_dept.setdefault(dept, []).append(s)

# ── Build SKILL-INDEX.md ──
lines = []
lines.append("# Skill Index")
lines.append("")
lines.append("*Auto-generated by build-skill-index.sh from skills.yml — do not edit manually*")
lines.append("")
lines.append(f"**{len(skills)} skills** across {len(by_dept)} departments")
lines.append("")
lines.append("| Skill | Department | Description | Model |")
lines.append("|-------|-----------|-------------|-------|")
for s in skills:
    lines.append(f"| `{s['name']}` | {s['department']} | {s['description']} | {s['model']} |")
lines.append("")
lines.append("## Example Invocations")
lines.append("")
for s in skills:
    ex = s.get("example_invocation", "")
    if ex:
        lines.append(f"- **{s['name']}**: _{ex}_")
lines.append("")
lines.append("## By Department")
lines.append("")

dept_order = ["engineering", "architecture", "quality", "workflow", "meta"]
for dept in dept_order:
    if dept not in by_dept:
        continue
    dept_skills = by_dept[dept]
    lines.append(f"### {dept.capitalize()} ({len(dept_skills)})")
    lines.append("")
    for s in dept_skills:
        tier_badge = f" [{s['model']}]" if s["model"] != "inherit" else ""
        chain = s.get("chain_after", [])
        chain_badge = f" → {', '.join(chain)}" if chain else ""
        lines.append(f"- `{s['name']}`{tier_badge} — {s['description']}{chain_badge}")
    lines.append("")

# Handle any departments not in the predefined order
for dept in sorted(by_dept.keys()):
    if dept in dept_order:
        continue
    dept_skills = by_dept[dept]
    lines.append(f"### {dept.capitalize()} ({len(dept_skills)})")
    lines.append("")
    for s in dept_skills:
        tier_badge = f" [{s['model']}]" if s["model"] != "inherit" else ""
        chain = s.get("chain_after", [])
        chain_badge = f" → {', '.join(chain)}" if chain else ""
        lines.append(f"- `{s['name']}`{tier_badge} — {s['description']}{chain_badge}")
    lines.append("")

with open(index_md, "w") as f:
    f.write("\n".join(lines))

print(f"Generated {index_md}")

# ── Build skill-routes.json ──
routes = {
    "skills": [],
    "by_department": {},
    "auto_triggers": {}
}

for s in skills:
    routes["skills"].append({
        "name": s["name"],
        "description": s["description"],
        "department": s["department"],
        "allowed-tools": s.get("allowed-tools", []),
        "model": s["model"],
        "chain_after": s.get("chain_after", []),
        "example_invocation": s.get("example_invocation", "")
    })

    dept = s["department"]
    routes["by_department"].setdefault(dept, []).append(s["name"])

    for pattern in s.get("auto_trigger_patterns", []):
        if pattern:
            routes["auto_triggers"][pattern] = s["name"]

# ── Load and validate workflows + bundles ──
config_dir = os.path.join(skills_dir, '..', 'config')
skill_names = {s["name"] for s in skills}
workflow_warnings = []
bundle_warnings = []

# Workflows
workflows_path = os.path.join(config_dir, 'workflows.yml')
workflows_data = []
if os.path.exists(workflows_path):
    with open(workflows_path, 'r') as f:
        wf_raw = yaml.safe_load(f)
    workflows_data = wf_raw.get('workflows', []) if wf_raw else []
    for wf in workflows_data:
        for step in wf.get('steps', []):
            for sid in step.get('recommendedSkills', []):
                if sid not in skill_names:
                    workflow_warnings.append(f"  WARNING: Workflow '{wf['id']}' step {step['step']} references unknown skill '{sid}'")
    print(f"Loaded {len(workflows_data)} workflows from {workflows_path}")
else:
    print(f"Info: {workflows_path} not found, skipping workflows")

# Bundles
bundles_path = os.path.join(config_dir, 'bundles.yml')
bundles_data = []
if os.path.exists(bundles_path):
    with open(bundles_path, 'r') as f:
        bd_raw = yaml.safe_load(f)
    bundles_data = bd_raw.get('bundles', []) if bd_raw else []
    bundle_ids = {b['id'] for b in bundles_data}
    for b in bundles_data:
        for sid in b.get('skills', []):
            if sid not in skill_names:
                bundle_warnings.append(f"  WARNING: Bundle '{b['id']}' references unknown skill '{sid}'")
        for inh in b.get('inherits', []):
            if inh not in bundle_ids:
                bundle_warnings.append(f"  WARNING: Bundle '{b['id']}' inherits unknown bundle '{inh}'")
    print(f"Loaded {len(bundles_data)} bundles from {bundles_path}")
else:
    print(f"Info: {bundles_path} not found, skipping bundles")

if workflow_warnings:
    print("Workflow validation warnings:")
    for w in workflow_warnings:
        print(w)
if bundle_warnings:
    print("Bundle validation warnings:")
    for w in bundle_warnings:
        print(w)

# Add to routes
routes["workflows"] = workflows_data
routes["bundles"] = {b["id"]: b for b in bundles_data}

with open(routes_json, "w") as f:
    json.dump(routes, f, indent=2)
    f.write("\n")

print(f"Generated {routes_json}")

# ── Generate WORKFLOW-INDEX.md ──
if workflows_data:
    workflow_index_path = os.path.join(skills_dir, 'WORKFLOW-INDEX.md')
    wf_lines = []
    wf_lines.append("# Workflow Index")
    wf_lines.append("")
    wf_lines.append("*Auto-generated by build-skill-index.sh from workflows.yml — do not edit manually*")
    wf_lines.append("")
    wf_lines.append(f"**{len(workflows_data)} workflows** across {len(set(w['category'] for w in workflows_data))} categories")
    wf_lines.append("")
    wf_lines.append("| Workflow | Category | Steps | Description |")
    wf_lines.append("|----------|----------|-------|-------------|")
    for wf in workflows_data:
        unique_skills = len(set(s for step in wf['steps'] for s in step.get('recommendedSkills', [])))
        wf_lines.append(f"| `{wf['id']}` | {wf['category']} | {len(wf['steps'])} ({unique_skills} skills) | {wf['description']} |")
    wf_lines.append("")

    # Group by category
    wf_by_cat = {}
    for wf in workflows_data:
        wf_by_cat.setdefault(wf['category'], []).append(wf)

    for cat in sorted(wf_by_cat.keys()):
        cat_display = cat.capitalize()
        wf_lines.append(f"## {cat_display}")
        wf_lines.append("")
        for wf in wf_by_cat[cat]:
            wf_lines.append(f"### {wf['name']} (`{wf['id']}`)")
            wf_lines.append("")
            wf_lines.append(f"{wf['description']}")
            wf_lines.append("")
            for step in wf['steps']:
                skills_str = ", ".join(f"`{s}`" for s in step.get('recommendedSkills', []))
                wf_lines.append(f"- **Step {step['step']}**: {step['title']} — {skills_str}")
            wf_lines.append("")
            wf_lines.append(f"*Invoke:* `/workflow {wf['id']}` or `/workflow {wf['id']} --start`")
            wf_lines.append("")

    # Bundles section
    if bundles_data:
        wf_lines.append("## Bundles")
        wf_lines.append("")
        wf_lines.append("| Bundle | Skills | Description |")
        wf_lines.append("|--------|--------|-------------|")
        for b in bundles_data:
            skill_count = len(b.get('skills', []))
            inherits_note = f" (+{', '.join(b['inherits'])})" if b.get('inherits') else ""
            wf_lines.append(f"| `{b['id']}` | {skill_count}{inherits_note} | {b['description']} |")
        wf_lines.append("")

    with open(workflow_index_path, 'w') as f:
        f.write("\n".join(wf_lines))
    print(f"Generated {workflow_index_path}")

# ── Auto-sync skill table (imported by CLAUDE.md via @.claude/skills/skill-table.md) ──
import re
skill_table_path = os.path.join(skills_dir, 'skill-table.md')
start_marker = "<!-- SKILL-TABLE-START -->"
end_marker = "<!-- SKILL-TABLE-END -->"
table_lines = ["| Skill | Description |", "|-------|-------------|"]
for skill in skills:
    table_lines.append(f"| `{skill['name']}` | {skill['description']} |")
new_table = "\n".join(table_lines)
with open(skill_table_path, 'w') as f:
    f.write(f"{start_marker}\n{new_table}\n{end_marker}\n")
print(f"Updated skill-table.md ({len(skills)} skills)")

print(f"Done: {len(skills)} skills indexed, {len(routes['auto_triggers'])} auto-trigger patterns")
PYEOF

# ── Also rebuild commands if build-commands.sh exists ──
BUILD_COMMANDS="$SCRIPT_DIR/build-commands.sh"
if [[ -x "$BUILD_COMMANDS" ]]; then
  echo ""
  echo "Rebuilding commands from commands.yml..."
  bash "$BUILD_COMMANDS" || echo "Warning: build-commands.sh failed" >&2
fi

echo "Build complete."
